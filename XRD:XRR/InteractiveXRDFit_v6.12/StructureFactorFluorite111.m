% Calculates the structure factor for materials with a fluorite structure
% AB_2 oriented 111
% Fluorite 3x4xAB2 => 3x12 atoms => 36 atoms => A12B24
%*********************************
function[F]=StructureFactorFluorite111(fA,fB,Q,d)

z(1)=0/12;   % 2 A
z(2)=1/12;   % 4 B
z(3)=3/12;   % 4 B
z(4)=4/12;   % 4 A
z(5)=5/12;   % 4 B
z(6)=7/12;   % 4 B
z(7)=8/12;   % 4 A
z(8)=9/12;   % 4 B
z(9)=11/12;  % 4 B
z(10)=12/12; % 2 A
F=2*fA.*exp(1i*(Q.*z(1).*d))...
    +4*fB.*exp(1i*(Q.*z(2).*d))...
    +4*fB.*exp(1i*(Q.*z(3).*d))...
    +4*fA.*exp(1i*(Q.*z(4).*d))...
    +4*fB.*exp(1i*(Q.*z(5).*d))...
    +4*fB.*exp(1i*(Q.*z(6).*d))...
    +4*fA.*exp(1i*(Q.*z(7).*d))...
    +4*fB.*exp(1i*(Q.*z(8).*d))...
    +4*fB.*exp(1i*(Q.*z(9).*d))...
    +2*fA.*exp(1i*(Q.*z(10).*d));
